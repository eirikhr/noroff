def addering(n1, n2):
    nummer = int(n1)+int(n2)
    return nummer


def subtraksjon(n1, n2):
    nummer = int(n1)-int(n2)
    return nummer


def gang(n1, n2):
    nummer = int(n1)*int(n2)
    return nummer


def deling(n1, n2):
    nummer = int(n1)/int(n2)
    return nummer


